"""
根据英雄名字推荐英雄的出装
存储英雄，以及对应的出装
"""
lst = [
    ['英雄名', ['出装1', '出装2', '出装3']],
    ['亚索', ['电刀', '攻速鞋', '无尽']]
]
name = '亚索'
# 时间复杂度为O(1)
for item in lst:
    if item[0] == name:
        print(item[1])

# 字典的时间复杂度为O(1)
